# PYTHON CHANGE LIST ITEMS
# 1. Change Item Value:
thisList = ['apple', 'orange', 'chicken', 'grapes', 'juice', 'watermelon', 'banana']
thisList[2] = 'blackcurrant'
print(thisList)

# 2. Change a Range of Item Values:
thisList[1:3] = ['ukulele', 'kiwi' ]
print(thisList)

# Insert Items:
# to insert a new list item, without replacing any of the existing values, we can use the insert() method.
L1 = ["APPLE", 'banana', 'cherry']
L1.insert(2, 'watermelon')
print(L1)

# as a result the list will contain now 4 total items.
